import XCTest
@testable import habitOS_mobile

final class ModelTests: XCTestCase {

    // MARK: - CoachMessage

    func testCoachMessageDecoding() throws {
        let json = """
        {
            "id": "AAAAAAAA-AAAA-AAAA-AAAA-AAAAAAAAAAAA",
            "profile_id": "BBBBBBBB-BBBB-BBBB-BBBB-BBBBBBBBBBBB",
            "role": "assistant",
            "channel": "app",
            "message_text": "Hola! Como va todo?",
            "created_at": "2026-01-15T10:30:00Z"
        }
        """.data(using: .utf8)!

        let decoder = JSONDecoder()
        decoder.dateDecodingStrategy = .iso8601
        let message = try decoder.decode(CoachMessage.self, from: json)

        XCTAssertEqual(message.role, .assistant)
        XCTAssertEqual(message.channel, "app")
        XCTAssertEqual(message.messageText, "Hola! Como va todo?")
        XCTAssertEqual(message.profileId.uuidString.uppercased(), "BBBBBBBB-BBBB-BBBB-BBBB-BBBBBBBBBBBB")
        XCTAssertNil(message.mediaType)
        XCTAssertNil(message.mediaUrl)
    }

    func testCoachMessageRoles() {
        XCTAssertEqual(MessageRole(rawValue: "user"), .user)
        XCTAssertEqual(MessageRole(rawValue: "assistant"), .assistant)
        XCTAssertEqual(MessageRole(rawValue: "system"), .system)
        XCTAssertNil(MessageRole(rawValue: "unknown"))
    }

    func testCoachMessageTextCompat() {
        let msg = CoachMessage(
            id: UUID(),
            profileId: UUID(),
            role: .user,
            channel: "app",
            messageText: "Test message",
            mediaType: nil,
            mediaUrl: nil,
            metadata: nil,
            expiresAt: nil,
            createdAt: Date()
        )
        // Backward-compat computed property
        XCTAssertEqual(msg.text, "Test message")
    }

    // MARK: - DailyTaskItem

    func testDailyTaskItemDecoding() throws {
        let json = """
        {
            "id": "CCCCCCCC-CCCC-CCCC-CCCC-CCCCCCCCCCCC",
            "user_id": "DDDDDDDD-DDDD-DDDD-DDDD-DDDDDDDDDDDD",
            "task_date": "2026-01-15T00:00:00Z",
            "title": "Beber 2L de agua",
            "category": "hydration",
            "is_completed": false,
            "sort_order": 1,
            "auto_generated": true,
            "source": "plan",
            "created_at": "2026-01-15T00:00:00Z"
        }
        """.data(using: .utf8)!

        let decoder = JSONDecoder()
        decoder.dateDecodingStrategy = .iso8601
        let task = try decoder.decode(DailyTaskItem.self, from: json)

        XCTAssertEqual(task.title, "Beber 2L de agua")
        XCTAssertEqual(task.category, "hydration")
        XCTAssertFalse(task.isCompleted)
        XCTAssertTrue(task.autoGenerated)
        XCTAssertEqual(task.source, "plan")
        XCTAssertNil(task.completedAt)
    }

    func testDailyTaskCategoryCompat() {
        // TaskCategory raw values should map from category strings
        XCTAssertEqual(DailyTaskItem.TaskCategory(rawValue: "nutrition"), .nutrition)
        XCTAssertEqual(DailyTaskItem.TaskCategory(rawValue: "hydration"), .hydration)
        XCTAssertEqual(DailyTaskItem.TaskCategory(rawValue: "activity"), .activity)
        XCTAssertNil(DailyTaskItem.TaskCategory(rawValue: "unknown"))
    }

