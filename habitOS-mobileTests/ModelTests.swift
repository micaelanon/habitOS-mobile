    // MARK: - CoachMemory

    func testCoachMemoryDecoding() throws {
        let json = """
        {
            "id": "11111111-1111-1111-1111-111111111111",
            "profile_id": "22222222-2222-2222-2222-222222222222",
            "fact_kind": "body",
            "title": "Peso",
            "fact_text": "Peso actual: 78 kg",
            "created_at": "2026-01-15T10:00:00Z"
        }
        """.data(using: .utf8)!

        let decoder = JSONDecoder()
        decoder.dateDecodingStrategy = .iso8601
        let memory = try decoder.decode(CoachMemory.self, from: json)

        XCTAssertEqual(memory.factKind, "body")
        XCTAssertEqual(memory.title, "Peso")
        XCTAssertEqual(memory.factText, "Peso actual: 78 kg")
        // Backward-compat computed properties
        XCTAssertEqual(memory.category, "Cuerpo")
        XCTAssertEqual(memory.fact, "Peso actual: 78 kg")
    }

    func testCoachMemoryCategoryMapping() {
        let testCases: [(factKind: String, expected: String)] = [
            ("body", "Cuerpo"),
            ("goal", "Objetivos"),
            ("preference", "Preferencias"),
            ("restriction", "Restricciones"),
            ("allergy", "Restricciones"),
            ("context", "Historial"),
            ("schedule", "Historial"),
            ("habit", "Hábitos"),  // Note: Spanish special char
            ("medical", "Médico"),  // Note: Spanish special char
            ("something_new", "Otros"),
        ]

        for tc in testCases {
            let memory = CoachMemory(
                id: UUID(),
                profileId: UUID(),
                factKind: tc.factKind,
                title: "Test",
                factText: "Test fact",
                createdAt: Date()
            )
            XCTAssertEqual(memory.category, tc.expected, "fact_kind '\(tc.factKind)' should map to '\(tc.expected)'")
        }
    }

